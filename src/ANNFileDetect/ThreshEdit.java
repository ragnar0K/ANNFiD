    public ThreshEdit(EditNet vader, boolean showFileType, String nnet, String fileType, String thresholds, SQLiteLib sqlClass) {
        initComponents();
        if (!showFileType) 
            FTPanel.setVisible(false);
        else 
            lastPlus.setVisible(false);
        start = "";
        end = "";
        net = nnet;
        type = fileType;
        thres = thresholds;
        sql = sqlClass;
        main = vader;
        mp = 0;
        if (thresholds.length() > 0) {
            String[] thstuff = thresholds.split("\\s");
            String[] values = thstuff[1].split("-");
            StartValue.setText(values[0]);
            EndValue.setText(values[1]);
            ScoreBox.setText(thstuff[3]);
            WeightBox.setText(thstuff[5]);
            FileType.setText(fileType);
            start = values[0];
            end = values[1];
        }
    }

    private Double hashDbl(Double dbl) {
        DecimalFormat df = new DecimalFormat("#0.00000");
        String tmp = df.format(dbl);
        return Double.valueOf(tmp);

    }
    
