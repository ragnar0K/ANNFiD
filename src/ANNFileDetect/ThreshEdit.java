    private void SendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendActionPerformed
        if (start.length() > 0 && end.length() > 0)
            sql.deleteSingleThreshold(type, net, start, end);
        if (type.length() == 0)
            type = FileType.getText();
        sql.addSingleThreshold(net, type, StartValue.getText() , EndValue.getText(), WeightBox.getText(), ScoreBox.getText());
        try {
        main.populateList();
        }
        catch (Exception exc) {
            System.out.println(exc.toString());
        }
        this.dispose();
    }//GEN-LAST:event_SendActionPerformed

